def _instantiate(b64_src):
    src = base64.b64decode(b64_src).decode()
    ns = {"__name__": "_sub"}
    exec(compile(src, "sub", "exec"), ns)
    if "logger" in ns:
        ns["logger"] = _Silent()
    return ns["Trader"]()


