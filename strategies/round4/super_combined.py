class Trader:
    def bid(self):
        return 0

    def run(self, state):
        try:
            store = json.loads(state.traderData) if state.traderData else {}
        except Exception:
            store = {}

        merged: dict = {}
        new_store = {}

        original_td = state.traderData
        saved_stdout = sys.stdout
        try:
            sys.stdout = StringIO()
            for key, sub in _SUBS.items():
                state.traderData = store.get(key, "")
                try:
                    sub_orders, _, sub_td = sub.run(state)
                except Exception:
                    sub_orders, sub_td = {}, ""
                new_store[key] = sub_td
                if not sub_orders:
                    continue
                for prod, ords in sub_orders.items():
                    if _OWNER.get(prod) == key and ords:
                        merged[prod] = ords
        finally:
            sys.stdout = saved_stdout
            state.traderData = original_td

        return merged, 0, json.dumps(new_store)
